function plus(a,b){
    let plus;
    plus = a + b;
    return plus; 
}

function minus(a,b){
    let minus;
    minus = a - b;
    return minus;
}

function times(a,b){
    let times;
    times = a * b;
    return times;
}

function div(a,b){
    let div;
    div = a / b;
    return div;
}

function mod(a,b){
    let mod;
    mod = a % b;
    return mod;
}
